void main() {
  // Declare a variable of type int.
  int age = 25;

  // Declare a variable of type double.
  double interestRate = 10.5;

  // Declare a variable of type num.
  num number = 20;

  // Declare a variable of type String.
  String string = "This is the strng";

  // Declare a variable of type String.
  bool boolean = true;

  // Declare a variable of type String.
  var name = "Alice";

  // Declare a list of Strings.
  List<String> names = ["Alice", "Bob", "Carol"];

  // Declare a set of int.
  Set<int> nums = {1, 5, -1};

  // Declare a map of Strings to ints.
  Map<String, int> ages = {"Alice": 25, "Bob": 30, "Carol": 35};

  // Declare a dynamic type variable.
  dynamic dy = 5;

  // Print the value of the `age` variable to the console.
  print(age); // 25

  // Print the value of the `interestRate` to the console.
  print(interestRate); // 10.5

  // Print the value of the `number` to the console.
  print(number);

  // Print the value of the `string` to the console.
  print(string);

  // Print the value of the `boolean` to the console.
  print(boolean);

  // Print the value of the `name` variable to the console.
  print(name); // Alice

  // Print the first element of the `names` list to the console.
  print(names[0]); // Alice

  // Print the first element of the `nums` list to the console.
  print(nums.first); // 1

  // Print the value of the `Alice` key in the `ages` map to the console.
  print(ages["Alice"]); // 25

  // Print the value of the `dy` to the console.
  print(dy); // 5
}
